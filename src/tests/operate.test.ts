/**
 * @module operate.test
 *
 * Тести для функції `operate`, яка виконує базові арифметичні операції.
 *
 * @remarks
 * Функція `operate` приймає три аргументи:
 * - перше число (рядок)
 * - друге число (рядок)
 * - оператор у вигляді символу (`+`, `-`, `*`, `/`)
 *
 * Повертає результат у вигляді рядка або `"Error"` при некоректній операції
 * (наприклад, ділення на нуль).
 *
 * Тести покривають:
 * - додавання
 * - віднімання
 * - множення
 * - ділення
 * - ділення на нуль
 * - обробку невідомої операції
 */

import { operate } from '../logic';

describe('operate', () => {

    /** Перевіряє операцію додавання. */
    test('додає два числа', () => {
        expect(operate('2', '3', '+')).toBe('5');
    });

    /** Перевіряє операцію віднімання. */
    test('віднімає два числа', () => {
        expect(operate('5', '3', '-')).toBe('2');
    });

    /** Перевіряє операцію множення. */
    test('множить два числа', () => {
        expect(operate('4', '3', '*')).toBe('12');
    });

    /** Перевіряє операцію ділення. */
    test('ділить два числа', () => {
        expect(operate('10', '2', '/')).toBe('5');
    });

    /**
     * Перевіряє захист від ділення на нуль.
     *
     * @expected "Error"
     */
    test('ділення на нуль повертає "Error"', () => {
        expect(operate('5', '0', '/')).toBe('Error');
    });

    /**
     * Перевіряє реакцію на невідомий оператор.
     *
     * @expected "0"
     */
    test('незнайома операція повертає "0"', () => {
        expect(operate('2', '3', '%')).toBe('0');
    });
});
